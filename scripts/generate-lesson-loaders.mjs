import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");
const contentRoot = path.join(rootDir, "content");
const outputPath = path.join(rootDir, "lib", "lesson-loaders.generated.ts");

const allowedExtensions = new Set([".ts", ".tsx", ".js", ".jsx"]);

function walk(directory) {
  const entries = fs.readdirSync(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(entryPath));
      continue;
    }

    if (allowedExtensions.has(path.extname(entry.name))) {
      files.push(entryPath);
    }
  }

  return files;
}

function toLoaderKey(filePath) {
  const relativePath = path.relative(contentRoot, filePath);
  const parts = relativePath.split(path.sep);

  if (parts.length !== 3) {
    return null;
  }

  const [subject, topic, fileName] = parts;
  const lesson = fileName.replace(/\.(tsx|ts|jsx|js)$/, "");
  return `${subject}/${topic}/${lesson}`;
}

function toImportSpecifier(filePath) {
  const relativePath = path.relative(path.join(rootDir, "lib"), filePath);
  return relativePath.split(path.sep).join("/").replace(/\.(tsx|ts|jsx|js)$/, "");
}

if (!fs.existsSync(contentRoot)) {
  throw new Error(`Content root not found: ${contentRoot}`);
}

const loaders = walk(contentRoot)
  .map((filePath) => {
    const key = toLoaderKey(filePath);
    if (!key) {
      return null;
    }

    return {
      key,
      specifier: toImportSpecifier(filePath),
    };
  })
  .filter(Boolean)
  .sort((left, right) => left.key.localeCompare(right.key));

const output = `/* eslint-disable */
// This file is auto-generated by scripts/generate-lesson-loaders.mjs.
// Do not edit manually.

export const LESSON_LOADERS: Record<string, () => Promise<{ default: any }>> = {
${loaders
  .map(
    ({ key, specifier }) =>
      `  ${JSON.stringify(key)}: () => import(${JSON.stringify(specifier)}),`
  )
  .join("\n")}
};
`;

if (!fs.existsSync(path.dirname(outputPath))) {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
}

const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : null;
if (existing !== output) {
  fs.writeFileSync(outputPath, output, "utf8");
  console.log(`Wrote ${path.relative(rootDir, outputPath)} with ${loaders.length} loaders.`);
} else {
  console.log(`${path.relative(rootDir, outputPath)} is up to date (${loaders.length} loaders).`);
}
